  end=$((SECONDS+5))
  while [ $SECONDS -lt $end ]; do
    col=$((RANDOM % cols))
    char=${chars[RANDOM % ${#chars[@]}]}
    
    tput cup $((RANDOM % rows)) $col
    echo -ne "\033[32m$char\033[0m"
    sleep 0.0001
  done
}
